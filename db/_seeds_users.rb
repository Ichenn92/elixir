                          last_name: attribute[:last_name],
                          email: attribute[:email],
                          password: attribute[:password])
      photo = attribute[:photo]
      user.photo.attach(io: URI.open(photo[:io]), filename: photo[:filename], content_type: photo[:content_type])

      puts "Signed up a new user nickname #{user.nickname}: #{user.first_name} #{user.last_name}"
    end
  end

